//Operators
//Unary operator
let a: string = "AKS VOID";
console.log("unary operator: ",a);

//Increment & Decrement
let v1: number = 10;
console.log("increment opertor:",v1);
console.log("v1++ : ",v1++);

let v2: number = 20;
console.log("decrement operator : ",v2);
console.log("--v2 : ",--v2);

//Arithmetic Operator
let v3: number = 30;
let v4: number = 50;
console.log("v3 + v4 : ",v3 + v4);
console.log("v4 - v3 : ",v4 - v3);
console.log("v3 * v4 : ",v3 * v4);
console.log("v3 / v4 : ",v3 / v4);
console.log("v3 % v4 : ",v3 % v4);

//Relational Operator
let v5: boolean = 15 > 20;
console.log("relational operator : 15 > 20 : ",v5);

//Assignment Operator
let v6: number = 25;
console.log("assignment operator : v6+=25",v6 += 25);

//Bit Wise Operator
let v7: number = 30;
let v8: number = 50;
console.log("Bit Wise Operator : 30 | 50",v7 | v8);

//Logical Operator
let v9: boolean = (10 > 20);
let v10: boolean = (22 > 12);
console.log("Logical : (10 > 20) && (22 > 12) : ",v9 && v10);

//Shift Operator
let v11: number = 111 << 5;
console.log("111 << 5",v11);

let v12: number = 17 >> 1;
console.log("17 >> 1",v12);