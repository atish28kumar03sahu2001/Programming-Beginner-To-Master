//Decision Control
let abool1 : boolean = 15 > 12;
if(abool1){
    console.log("Yes");
} else {
    console.log("No");
}

//Looping Concept
for(let i: number = 10; i <= 15; i++){
    console.log(i);
}
let j: number = 5;
while(j <= 10) {
    console.log(j);
    j++;
}
let k = 15;
do{
    console.log(k);
    k++
}while(k <= 20);