//Bitwise Operator
console.log(`(10 & 20) : ${(10 & 20)}`);
console.log(`20 | 30 : ${(20 | 30)}`);
console.log(`40 ^ 50 : ${(40 ^ 50)}`);

console.log(`!(10 & 20) : ${!(10 & 20)}`);
console.log(`!(20 | 30) : ${!(20 | 30)}`);
console.log(`!(40 ^ 50) : ${!(40 ^ 50)}`);

console.log(`(~10 <-> ~20) : ${~(10)} <-> ${~(20)}`);