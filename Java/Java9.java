//Relational Operator
public class Java9 {
    public static void main(String args[]){
        System.out.println("20 > 15 : "+(20 > 15));
        System.out.println("25 < 15 : "+(25 < 15));

        System.out.println("15 >= 20 : "+(15 >= 20));
        System.out.println("25 <= 20 : "+(25 <= 20));

        System.out.println("15 == 15 : "+(15 == 15));
        System.out.println("20 != 20 : "+(20 != 20));
    }
}