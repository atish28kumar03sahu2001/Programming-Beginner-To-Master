public class Java23 {
    public static void main(String args[]) {
        for(int i = 10; i <= 20; i++){
            System.out.println(i);
        }
        int j = 5;
        while(j <= 10){
            System.out.println(j);
            j++;
        }
        int k = 10;
        do{
            System.out.println(k);
            k++;
        }while(k <= 15);
    }
}