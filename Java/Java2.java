public class Java2 {
    public static void main(String args []) {
        byte num1 = -128;
        System.out.println("byte num1 : "+num1);

        byte num2 = 127;
        System.out.println("byte num2 : "+num2);

        short num3 = -32768;
        System.out.println("short num3 : "+num3);

        short num4 = 32767;
        System.out.println("short num4 : "+num4);

        int num5 = -2147483648;
        System.out.println("int num5 : "+num5);

        int num6 = 2147483647;
        System.out.println("int num6 : "+num6);

        long num7 = -9223372036854775808L;
        System.out.println("long num7 : "+num7);

        long num8 = 9223372036854775807L;
        System.out.println("Long num8 : "+num8);

        float num9 = (float)0.123456789;
        System.out.println("Float num9 : "+num9);

        double num10 = (double)0.1234567890123456789;
        System.out.println("Double num10 : "+num10);

    }
}