//Arithmetic Operator
public class Java8 {
    public static void main(String args[]){
        System.out.println("10 + 20 : "+(10 + 20));
        System.out.println(" 10 - 20 : "+(10 - 20));

        System.out.println("10 * 20 : "+(10 * 20));
        System.out.println("10 / 20 : "+(10 / 20));

        System.out.println("10 % 20 : "+(10 % 20));
    }
}